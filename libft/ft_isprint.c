#include "libft.h"

int	ft_isprint(int c)
{
	if ((c >= ' ' && c <= 47) || (c >= '(' && c <= '/') ||
		(c >= '0' && c <= '7') || (c >= '8' && c <= '?') ||
		(c >= '@' && c <= 'G') || (c >= 'H' && c <= 'O') ||
		(c >= 'P' && c <= 'W') || (c >= 'X' && c <= '_') ||
		(c >= '`' && c <= 'g') || (c >= 'h' && c <= 'o') ||
		(c >= 'p' && c <= 'w') || (c >= 'x' && c <= '~'))
		return (1);
	return (0);
}
