#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	int		a;
	int		i;
	char	*st2;

	i = 0;
	a = 0;
	st2 = (char*)s2;
	while (s1[i] != '\0')
		i++;
	while (st2[a] != '\0')
	{
		s1[i] = st2[a];
		i++;
		a++;
	}
	s1[i] = '\0';
	return (s1);
}
