#include "libft.h"

char	*ft_strdup(const char *src)
{
	char	*str;
	size_t	l;
	size_t	i;

	l = ft_strlen(src);
	i = 0;
	if (l + 1 == 0)
		return (0);
	str = (char*)malloc(sizeof(*src) * (l + 1));
	if (str == NULL)
		return (NULL);
	while (src[i] != '\0')
	{
		str[i] = src[i];
		i++;
	}
	str[i] = '\0';
	return (str);
}
